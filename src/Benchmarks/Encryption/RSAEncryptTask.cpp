	RSAEncryptTask::RSAEncryptTask()
			: _context(nullptr), _key(nullptr)
	{
	}

	RSAEncryptTask::~RSAEncryptTask()
	{
		if (_context != nullptr)
		{
			EVP_PKEY_CTX_free(_context);
		}

		if (_key != nullptr)
		{
			EVP_PKEY_free(_key);
			_key = nullptr;
		}
	}

